package Home_Task_15_Multithreding;

public class TypeAuto {
    private String type;

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    @Override
    public String toString() {
        return "TypeAuto{" +
                "type='" + type + '\'' +
                '}';
    }
}
